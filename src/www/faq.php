                        <div id="collapseaboutfour" class="panel-collapse collapseFAQ collapse" aria-expanded="false" style="height: 0px;">
                            <div class="panel-body">
                                <p>DigiLocker is safe and secure to use. We are taking all precautionary measures to ensure your data is protected and  uncompromised. Please find the list of security measures that have been implemented till now. This list will expand as more safety features get added. </p>
                                <ul type="i">
                                    <li><strong>256 Bit SSL Encryption –</strong>DigiLocker uses 256 bit secure socket layer (SSL) Encryption for information transmitted during any activity.</li>
                                    <li><strong>Biometrics based &amp; Mobile Authentication based Sign Up -</strong>
                                    Digital Locker uses the UIDAI provided Aadhaar system for authenticating users and allowing access to the platform. The Aadhaar system offers two authentication options                                    <ol type"i"="">
                                    	<li>Biometric based via fingerprint scanning,</li>
                                        <li>Mobile authentication based via OTP (one time password).</li>
                                     </ol>
                                
                                 This makes the system very secure and prevents any chance of unauthorized access. </li>
                                 <li><strong>ISO 27001 certified Data Centre  -</strong>
                                    The application is hosted in a ISO 27001 security certified data centre.</li>
                                    <li><strong>Data Redundancy  -</strong>
                                    Data is backed up in secure environment with proper redundancy.</li>
                                    <li><strong>Timed Log Out –</strong>
                                    To protect citizen’s account from unauthorized access, our system is designed to terminate session automatically if extended inactivity is detected.</li>
                                    <li><strong>Security Audit -</strong>
                                    The DigiLocker application has been security audited by a recognized audit agency and the application security audit certificate has been obtained.</li>
                                  </ul>
